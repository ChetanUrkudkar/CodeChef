package practice;

import java.util.*;
public class ShoppingChange {
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		
		int t = input.nextInt();
		
		while (t --> 0) {
			int x = input.nextInt();
			
			System.out.println(100-x);
		}
	}
}
