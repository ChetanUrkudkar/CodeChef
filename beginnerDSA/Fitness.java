package beginnerDSA;
import java.util.*;

public class Fitness {
	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		int t = input.nextInt();
		
		while (t --> 0) {
			int x = input.nextInt();
			System.out.println((x * 2) * 5);
		}
	}
}
